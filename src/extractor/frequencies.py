def noise(key, fineAdjust=0):
    # fineAdjust is ignored
    if key <= 20:
        key = 0
    else:
        key -= 21
        if key > 59:
            key = 59
    freq = gNoiseTable[key]
    r = (freq & 7) or 0.5
    s = (freq & 0xf0) >> 4
    real_freq = 524288 / (r * 2 ** (s + 1))
    return real_freq

